public class Potions extends Object {

    int ammountHealed = 0;


    public Object smallPotion(){
        ammountHealed = 25;
        return smallPotion();
    }


    public Object mediumPotion(){
        ammountHealed =50;
        return mediumPotion();
    }

    public Object largePotion(){
        ammountHealed =100;
        return largePotion();
    }




}
